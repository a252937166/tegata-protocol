export type TKey = keyof (typeof dict)['en'];

const LangCtx = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: (k: TKey) => string }>({
  lang: 'en',
  setLang: () => {},
  t: (k) => k,
});

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => (localStorage.getItem('tegata.lang') as Lang) || 'en');
  useEffect(() => {
    localStorage.setItem('tegata.lang', lang);
    document.documentElement.lang = lang;
  }, [lang]);
  const t = (k: TKey) => dict[lang][k] ?? dict.en[k] ?? k;
  return <LangCtx.Provider value={{ lang, setLang, t }}>{children}</LangCtx.Provider>;
}

export const useLang = () => useContext(LangCtx);
